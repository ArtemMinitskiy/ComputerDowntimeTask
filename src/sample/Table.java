public class Table {

    private int i;
    private int tip;
    private int tio;
    private int tp;
    private int tno;
    private int too;
    private int to;
    private int tpr;
    private int k;

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getK() {
        return k;
    }

    public void setK(int k) {
        this.k = k;
    }

    public int getTIP() {
        return tip;
    }

    public void setTIP(int tip) {
        this.tip = tip;
    }

    public int getTIO() {
        return tio;
    }

    public void setTIO(int tio) {
        this.tio = tio;
    }

    public int getTP() {
        return tp;
    }

    public void setTP(int tp) {
        this.tp = tp;
    }

    public int getTNO() {
        return tno;
    }

    public void setTNO(int tno) {
        this.tno = tno;
    }

    public int getTOO() {
        return too;
    }

    public void setTOO(int too) {
        this.too = too;
    }

    public int getTO() {
        return to;
    }

    public void setTO(int to) {
        this.to = to;
    }

    public int getTPR() {
        return tpr;
    }

    public void setTPR(int tpr) {
        this.tpr = tpr;
    }

    public Table(int i, int k, int tip, int tio, int tp, int tno, int too, int to, int tpr) {
        this.i = i;
        this.k = k;
        this.tip = tip;
        this.tio = tio;
        this.tp = tp;
        this.tno = tno;
        this.too = too;
        this.to = to;
        this.tpr = tpr;

    }



}